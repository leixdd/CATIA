				<p>On behalf of TESDA Secretary Guiling “Gene” A. Mamondiong, Deputy Director General for Policies and Planning Rosanna A. Urdaneta led a press conference today, July 27, and presented the Agency’s long term plans for thwarting poverty by increasing the marginalized sector’s access to technical vocational education and training (TVET).<br><br>

