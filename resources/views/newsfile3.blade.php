				DDG Urdaneta clarified that aside from the training given by more than 4,000 TVET providers in the country, TESDA plans to bolster its training efforts using approaches such as community-based training, expanded scholarships, Mobile Training Program, TESDA Online Program, Barangay Empowerment thru TVET, CMUs (Compact Mobile Units) and Onsite Training and Assessment.<br><br>

				The Deputy Chief added that the 12-point agenda would serve as the framework on which programs designed specifically for expanded access to TVET by the identified groups would be implemented.   Further, she admitted that the planned steps would require very close coordination with relevant government agencies (such as the Department of Social Welfare and Development, Department of Agriculture, Philippine National Police among others), local government units, social organizations and private training centers.</p>
