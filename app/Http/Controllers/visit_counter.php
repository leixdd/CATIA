    public function tick(Request $request){
      $ip = $_SERVER['REMOTE_ADDR'];
      $vd = \Carbon\Carbon::now()->toDateString();
      $vt = \Carbon\Carbon::now('GMT+8')->toTimeString();

      $tick = new ticker(0, $ip, $vd, $vt);
      $this->dispatch($tick);
      return 'Ok';
    }
